#!/bin/bash

REVISION="$(git log --pretty=format:'%h' -n 1)"
PARENT=`readlink -f .`
INITRAMFS=$PARENT/compiled

if [ -e $INITRAMFS/busybox-1.25.0.zip ]; then
	echo "**************************************************************"
	echo "**************************************************************"
	echo "             Pushing busybox-1.25.0.zip To Device             "
	echo "**************************************************************"
	echo "**************************************************************"

	adb push $INITRAMFS/busybox-1.25.0.zip /sdcard/
	
	echo "**************************************************************"
	echo "**************************************************************"
	echo "               Rebooting Device Into Recovery                 "
	echo "**************************************************************"
	echo "**************************************************************"	
	
	adb reboot recovery
else
	echo "**************************************************************"
	echo "**************************************************************"
	echo "                 You Need To Compile First!!!                 "
	echo "                   !!!Nothing To Push!!!                      "
	echo "**************************************************************"
	echo "**************************************************************"
fi
